function initResizing(win, resizer) {
    resizer.addEventListener('mousedown', function(e) {
        e.preventDefault();
        window.addEventListener('mousemove', resize);
        window.addEventListener('mouseup', stopResize);
    });

    function resize(e) {
        // Calcul par rapport aux coordonnées de la souris et la position de la fenêtre
        const rect = win.getBoundingClientRect();
        const newWidth = e.clientX - rect.left;
        const newHeight = e.clientY - rect.top;
        
        // Applique uniquement si supérieur aux minimums
        if (newWidth > 200) win.style.width = newWidth + 'px';
        if (newHeight > 150) win.style.height = newHeight + 'px';
    }

    function stopResize() {
        window.removeEventListener('mousemove', resize);
        window.removeEventListener('mouseup', stopResize);
    }
}
